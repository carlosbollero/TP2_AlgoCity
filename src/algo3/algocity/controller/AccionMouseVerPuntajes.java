
package algo3.algocity.controller;

import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;

import algo3.algocity.view.VentanaInicial;

public class AccionMouseVerPuntajes implements ActionListener {

	VentanaInicial ventanaPortadora;
	
	
	public AccionMouseVerPuntajes(VentanaInicial ventana){
		this.ventanaPortadora = ventana;	
	}
	
	@Override
	public void actionPerformed(ActionEvent e) {
		this.ventanaPortadora.mostrarVistaPuntajes();
		
	}

}
